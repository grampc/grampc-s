/** Integral cost l(t,x(t),u(t),p,xdes,udes,userparam) 
    -------------------------------------------------- **/
void Chain_3_ProblemDescription::lfct(VectorRef out, ctypeRNum t, VectorConstRef x, VectorConstRef u, VectorConstRef p, const typeGRAMPCparam *param)
{
    

    out[0] = pCost_[2]*(u[0]*u[0])+pCost_[2]*(u[1]*u[1])+pCost_[2]*(u[2]*u[2])+pCost_[1]*(x[3]*x[3]+x[4]*x[4]+x[5]*x[5])+pCost_[1]*(x[9]*x[9]+x[10]*x[10]+x[11]*x[11])+pCost_[0]*(POW2(x[12]-3.0/2.0)+x[13]*x[13]+x[14]*x[14]);
}


/** Gradient dl/dx **/
void Chain_3_ProblemDescription::dldx(VectorRef out, ctypeRNum t, VectorConstRef x, VectorConstRef u, VectorConstRef p, const typeGRAMPCparam *param)
{
    

    out[0] = 0.0;
    out[1] = 0.0;
    out[2] = 0.0;
    out[3] = pCost_[1]*x[3]*2.0;
    out[4] = pCost_[1]*x[4]*2.0;
    out[5] = pCost_[1]*x[5]*2.0;
    out[6] = 0.0;
    out[7] = 0.0;
    out[8] = 0.0;
    out[9] = pCost_[1]*x[9]*2.0;
    out[10] = pCost_[1]*x[10]*2.0;
    out[11] = pCost_[1]*x[11]*2.0;
    out[12] = pCost_[0]*(x[12]*2.0-3.0);
    out[13] = pCost_[0]*x[13]*2.0;
    out[14] = pCost_[0]*x[14]*2.0;
}


/** Gradient dl/du **/
void Chain_3_ProblemDescription::dldu(VectorRef out, ctypeRNum t, VectorConstRef x, VectorConstRef u, VectorConstRef p, const typeGRAMPCparam *param)
{
    

    out[0] = pCost_[2]*u[0]*2.0;
    out[1] = pCost_[2]*u[1]*2.0;
    out[2] = pCost_[2]*u[2]*2.0;
}


