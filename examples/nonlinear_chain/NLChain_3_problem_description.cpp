/** OCP dimensions: states (Nx), controls (Nu), parameters (Np), equalities (Ng),
    inequalities (Nh), terminal equalities (NgT), terminal inequalities (NhT) **/
void Chain_3_ProblemDescription::ocp_dim(typeInt *Nx, typeInt *Nu, typeInt *Np, typeInt *Ng, typeInt *Nh, typeInt *NgT, typeInt *NhT)
{
    *Nx  = 15;
    *Nu  = 3;
    *Np  = 0;
    *Nh  = 0;
    *Ng  = 0;
    *NgT = 0;
    *NhT = 0;
}


/** System function f(t,x,u,p,userparam)
    ------------------------------------ **/
void Chain_3_ProblemDescription::ffct(VectorRef out, ctypeRNum t, VectorConstRef x, VectorConstRef u, VectorConstRef p, const typeGRAMPCparam *param)
{
    ctypeRNum  t2 = x[0]*x[0];
    ctypeRNum  t3 = x[1]*x[1];
    ctypeRNum  t4 = x[2]*x[2];
    ctypeRNum  t5 = -x[6];
    ctypeRNum  t6 = -x[7];
    ctypeRNum  t7 = -x[8];
    ctypeRNum  t8 = -x[12];
    ctypeRNum  t9 = -x[13];
    ctypeRNum  t10 = -x[14];
    ctypeRNum  t11 = t5+x[0];
    ctypeRNum  t12 = t6+x[1];
    ctypeRNum  t13 = t7+x[2];
    ctypeRNum  t14 = t8+x[6];
    ctypeRNum  t15 = t9+x[7];
    ctypeRNum  t16 = t10+x[8];
    ctypeRNum  t23 = t2+t3+t4;
    ctypeRNum  t17 = t11*t11;
    ctypeRNum  t18 = t12*t12;
    ctypeRNum  t19 = t13*t13;
    ctypeRNum  t20 = t14*t14;
    ctypeRNum  t21 = t15*t15;
    ctypeRNum  t22 = t16*t16;
    ctypeRNum  t24 = 1.0/sqrt(t23);
    ctypeRNum  t25 = t24*(1.1E+1/4.0E+2);
    ctypeRNum  t26 = t17+t18+t19;
    ctypeRNum  t27 = t20+t21+t22;
    ctypeRNum  t28 = t25-3.0/2.0E+1;
    ctypeRNum  t29 = 1.0/sqrt(t26);
    ctypeRNum  t30 = 1.0/sqrt(t27);
    ctypeRNum  t31 = t29*(1.1E+1/4.0E+2);
    ctypeRNum  t32 = t30*(1.1E+1/4.0E+2);
    ctypeRNum  t33 = t31-3.0/2.0E+1;
    ctypeRNum  t34 = t32-3.0/2.0E+1;
    ctypeRNum  t35 = t11*t33*(1.0E+2/3.0);
    ctypeRNum  t36 = t12*t33*(1.0E+2/3.0);
    ctypeRNum  t37 = t13*t33*(1.0E+2/3.0);
    out[0] = x[3];
    out[1] = x[4];
    out[2] = x[5];
    out[3] = t35+t28*x[0]*(1.0E+2/3.0);
    out[4] = t36+t28*x[1]*(1.0E+2/3.0);
    out[5] = t37+t28*x[2]*(1.0E+2/3.0)-9.81E+2/1.0E+2;
    out[6] = x[9];
    out[7] = x[10];
    out[8] = x[11];
    out[9] = -t35+t14*t34*(1.0E+2/3.0);
    out[10] = -t36+t15*t34*(1.0E+2/3.0);
    out[11] = -t37+t16*t34*(1.0E+2/3.0)-9.81E+2/1.0E+2;
    out[12] = u[0];
    out[13] = u[1];
    out[14] = u[2];
}


/** Jacobian df/dx multiplied by vector vec, i.e. (df/dx)^T*vec or vec^T*(df/dx) **/
void Chain_3_ProblemDescription::dfdx_vec(VectorRef out, ctypeRNum t, VectorConstRef x, VectorConstRef u, VectorConstRef p, VectorConstRef vec, const typeGRAMPCparam *param)
{
    ctypeRNum  t2 = x[0]*2.0;
    ctypeRNum  t3 = x[1]*2.0;
    ctypeRNum  t4 = x[2]*2.0;
    ctypeRNum  t5 = x[6]*2.0;
    ctypeRNum  t6 = x[7]*2.0;
    ctypeRNum  t7 = x[8]*2.0;
    ctypeRNum  t8 = x[12]*2.0;
    ctypeRNum  t9 = x[13]*2.0;
    ctypeRNum  t10 = x[14]*2.0;
    ctypeRNum  t11 = x[0]*x[0];
    ctypeRNum  t12 = x[1]*x[1];
    ctypeRNum  t13 = x[2]*x[2];
    ctypeRNum  t14 = x[6]*x[6];
    ctypeRNum  t15 = x[7]*x[7];
    ctypeRNum  t16 = x[8]*x[8];
    ctypeRNum  t17 = -x[6];
    ctypeRNum  t19 = -x[7];
    ctypeRNum  t21 = -x[8];
    ctypeRNum  t23 = -x[12];
    ctypeRNum  t25 = -x[13];
    ctypeRNum  t27 = -x[14];
    ctypeRNum  t29 = vec[9]*x[6]*1.1E+1;
    ctypeRNum  t30 = vec[10]*x[7]*1.1E+1;
    ctypeRNum  t31 = vec[11]*x[8]*1.1E+1;
    ctypeRNum  t32 = vec[9]*x[12]*1.1E+1;
    ctypeRNum  t33 = vec[10]*x[13]*1.1E+1;
    ctypeRNum  t34 = vec[11]*x[14]*1.1E+1;
    ctypeRNum  t18 = -t5;
    ctypeRNum  t20 = -t6;
    ctypeRNum  t22 = -t7;
    ctypeRNum  t24 = -t8;
    ctypeRNum  t26 = -t9;
    ctypeRNum  t28 = -t10;
    ctypeRNum  t35 = -t32;
    ctypeRNum  t36 = -t33;
    ctypeRNum  t37 = -t34;
    ctypeRNum  t38 = t17+x[0];
    ctypeRNum  t39 = t19+x[1];
    ctypeRNum  t40 = t21+x[2];
    ctypeRNum  t41 = t23+x[6];
    ctypeRNum  t42 = t25+x[7];
    ctypeRNum  t43 = t27+x[8];
    ctypeRNum  t56 = t11+t12+t13;
    ctypeRNum  t44 = t2+t18;
    ctypeRNum  t45 = t3+t20;
    ctypeRNum  t46 = t4+t22;
    ctypeRNum  t47 = t5+t24;
    ctypeRNum  t48 = t6+t26;
    ctypeRNum  t49 = t7+t28;
    ctypeRNum  t50 = t38*t38;
    ctypeRNum  t51 = t39*t39;
    ctypeRNum  t52 = t40*t40;
    ctypeRNum  t53 = t41*t41;
    ctypeRNum  t54 = t42*t42;
    ctypeRNum  t55 = t43*t43;
    ctypeRNum  t57 = 1.0/sqrt(t56);
    ctypeRNum  t58 = t57*t57*t57;
    ctypeRNum  t59 = t57*(1.1E+1/1.2E+1);
    ctypeRNum  t64 = t50+t51+t52;
    ctypeRNum  t65 = t53+t54+t55;
    ctypeRNum  t60 = -t59;
    ctypeRNum  t61 = t58*x[0]*x[1]*(1.1E+1/1.2E+1);
    ctypeRNum  t62 = t58*x[0]*x[2]*(1.1E+1/1.2E+1);
    ctypeRNum  t63 = t58*x[1]*x[2]*(1.1E+1/1.2E+1);
    ctypeRNum  t66 = pow(t65,3.0/2.0);
    ctypeRNum  t67 = 1.0/sqrt(t64);
    ctypeRNum  t69 = 1.0/sqrt(t65);
    ctypeRNum  t68 = t67*t67*t67;
    ctypeRNum  t70 = 1.0/t66;
    ctypeRNum  t71 = t67*(1.1E+1/1.2E+1);
    ctypeRNum  t72 = -t71;
    ctypeRNum  t73 = t38*t39*t68*(1.1E+1/1.2E+1);
    ctypeRNum  t74 = t38*t40*t68*(1.1E+1/1.2E+1);
    ctypeRNum  t75 = t39*t40*t68*(1.1E+1/1.2E+1);
    ctypeRNum  t76 = t38*t44*t68*(1.1E+1/2.4E+1);
    ctypeRNum  t77 = t39*t45*t68*(1.1E+1/2.4E+1);
    ctypeRNum  t78 = t40*t46*t68*(1.1E+1/2.4E+1);
    ctypeRNum  t79 = t61+t73;
    ctypeRNum  t80 = t62+t74;
    ctypeRNum  t81 = t63+t75;
    ctypeRNum  t82 = t72+t76+5.0;
    ctypeRNum  t83 = t72+t77+5.0;
    ctypeRNum  t84 = t72+t78+5.0;
    out[0] = t73*vec[10]-t79*vec[4]+t74*vec[11]-t80*vec[5]+t82*vec[9]-vec[3]*(t60+t82+t11*t58*(1.1E+1/1.2E+1)+5.0);
    out[1] = t73*vec[9]-t79*vec[3]+t75*vec[11]-t81*vec[5]+t83*vec[10]-vec[4]*(t60+t83+t12*t58*(1.1E+1/1.2E+1)+5.0);
    out[2] = t74*vec[9]-t80*vec[3]+t75*vec[10]-t81*vec[4]+t84*vec[11]-vec[5]*(t60+t84+t13*t58*(1.1E+1/1.2E+1)+5.0);
    out[3] = vec[0];
    out[4] = vec[1];
    out[5] = vec[2];
    out[6] = vec[9]*-1.0E+1+t73*vec[4]+t69*vec[9]*(1.1E+1/1.2E+1)+t74*vec[5]+t71*vec[9]+t82*vec[3]-vec[10]*(t73+t42*t47*t70*(1.1E+1/2.4E+1))-vec[11]*(t74+t43*t47*t70*(1.1E+1/2.4E+1))-t38*t44*t68*vec[9]*(1.1E+1/2.4E+1)-t41*t47*t70*vec[9]*(1.1E+1/2.4E+1);
    out[7] = vec[10]*-1.0E+1+t73*vec[3]+t69*vec[10]*(1.1E+1/1.2E+1)+t75*vec[5]+t71*vec[10]+t83*vec[4]-vec[9]*(t73+t41*t48*t70*(1.1E+1/2.4E+1))-vec[11]*(t75+t43*t48*t70*(1.1E+1/2.4E+1))-t39*t45*t68*vec[10]*(1.1E+1/2.4E+1)-t42*t48*t70*vec[10]*(1.1E+1/2.4E+1);
    out[8] = vec[11]*-1.0E+1+t74*vec[3]+t75*vec[4]+t69*vec[11]*(1.1E+1/1.2E+1)+t71*vec[11]+t84*vec[5]-vec[9]*(t74+t41*t49*t70*(1.1E+1/2.4E+1))-vec[10]*(t75+t42*t49*t70*(1.1E+1/2.4E+1))-t40*t46*t68*vec[11]*(1.1E+1/2.4E+1)-t43*t49*t70*vec[11]*(1.1E+1/2.4E+1);
    out[9] = vec[6];
    out[10] = vec[7];
    out[11] = vec[8];
    out[12] = (t70*(t14*vec[9]*1.1E+1+t66*vec[9]*6.0E+1+t30*x[6]+t31*x[6]-vec[9]*(t14+t54+t55)*1.1E+1-vec[10]*x[6]*x[13]*1.1E+1-vec[11]*x[6]*x[14]*1.1E+1))/1.2E+1-(t70*x[12]*(t30+t31+t36+t37))/1.2E+1;
    out[13] = (t70*(t15*vec[10]*1.1E+1+t66*vec[10]*6.0E+1+t29*x[7]+t31*x[7]-vec[10]*(t15+t53+t55)*1.1E+1-vec[9]*x[7]*x[12]*1.1E+1-vec[11]*x[7]*x[14]*1.1E+1))/1.2E+1-(t70*x[13]*(t29+t31+t35+t37))/1.2E+1;
    out[14] = (t70*(t16*vec[11]*1.1E+1+t66*vec[11]*6.0E+1+t29*x[8]+t30*x[8]-vec[11]*(t16+t53+t54)*1.1E+1-vec[9]*x[8]*x[12]*1.1E+1-vec[10]*x[8]*x[13]*1.1E+1))/1.2E+1-(t70*x[14]*(t29+t30+t35+t36))/1.2E+1;
}


